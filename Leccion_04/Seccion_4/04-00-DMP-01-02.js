//Ejemplo: numero convertirlo a texto (usamos el else if)

let numero = 2;
if (numero == 1) {
    console.log("Numero uno");
} else if (numero == 2) { //else if se usa cuando se quiere preguntar por mas opciones
    console.log("Numero dos");
} else if (numero == 3) {
    console.log("Numero tres");
} else if (numero == 4) {
    console.log("Numero cuatro");
} else {
    console.log("Numero desconocido");
}